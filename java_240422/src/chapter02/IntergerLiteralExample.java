package chapter02;

public class IntergerLiteralExample {
	public static void main(String[] args) {
//		0b~ : 이진수
		int var1=0b1011;
//		0~ : 8진수
//		2*8의승+0*8의1승+6*8의0승
		int var2=0206;
		int var3=365;
//		16진수
//		B(=11)*16의1승+3*16의0승
		int var4=0xB3;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
	
}
}
